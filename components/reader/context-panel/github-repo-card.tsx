interface GitHubRepoCardProps {
  repo: GitHubRepo;
  isSelected: boolean;
  onToggleSelect: (fullName: string) => void;
  onShowDetail: (repo: GitHubRepo) => void;
}

/**
 * Card component for displaying a GitHub repository.
 *
 * Supports selection and detail view expansion.
 */
export function GitHubRepoCard({
  repo,
  isSelected,
  onToggleSelect,
  onShowDetail,
}: GitHubRepoCardProps) {
  return (
    <div
      className={cn(
        "flex h-[100px] w-[320px] items-start gap-3 rounded-lg border p-3 transition-colors",
        isSelected ? "border-primary bg-primary/5" : "border-border",
      )}
    >
      {/* Selection checkbox */}
      <button
        type="button"
        onClick={() => onToggleSelect(repo.fullName)}
        className={cn(
          "mt-0.5 flex h-4 w-4 shrink-0 cursor-pointer items-center justify-center rounded-sm border transition-colors",
          isSelected
            ? "border-primary bg-primary text-primary-foreground"
            : "border-muted-foreground/30 hover:border-muted-foreground",
        )}
      >
        {isSelected && <Check className="h-3 w-3" />}
      </button>

      {/* Repo info */}
      <div className="min-w-0 flex-1 space-y-1.5">
        {/* Header: GitHub icon + repo name + stars + expand */}
        <div className="flex items-center gap-1.5">
          <GitHubIcon className="h-4 w-4 shrink-0 text-muted-foreground" />
          <span className="min-w-0 flex-1 truncate text-sm font-medium">
            {repo.fullName}
          </span>
          <span className="flex shrink-0 items-center gap-0.5 text-xs text-muted-foreground">
            <Star className="h-3.5 w-3.5" />
            {repo.stars >= 1000
              ? `${(repo.stars / 1000).toFixed(1)}k`
              : repo.stars}
          </span>
          <button
            type="button"
            onClick={() => onShowDetail(repo)}
            className="ml-1 shrink-0 rounded p-0.5 text-muted-foreground hover:bg-accent hover:text-foreground"
            title="Show details"
          >
            <Maximize2 className="h-3.5 w-3.5" />
          </button>
        </div>

        {/* Description */}
        {repo.description && (
          <p className="line-clamp-2 text-xs text-muted-foreground">
            {repo.description}
          </p>
        )}

        {/* Footer: language + website */}
        <div className="flex items-center gap-3 text-xs text-muted-foreground">
          {repo.language && <span>{repo.language}</span>}
          {repo.homepage && (
            <a
              href={repo.homepage}
              target="_blank"
              rel="noopener noreferrer"
              className="flex items-center gap-0.5 text-primary hover:underline"
            >
              <ExternalLink className="h-3 w-3" />
              <span className="max-w-[150px] truncate">
                {repo.homepage.replace(/^https?:\/\/(www\.)?/, "")}
              </span>
            </a>
          )}
        </div>
      </div>
    </div>
  );
}
